type TranslationKey = keyof typeof translations.th

interface I18nContextType {
  lang: Lang
  setLang: (lang: Lang) => void
  t: (key: TranslationKey) => string
  tLeaveType: (typeCode: string, dbName?: string) => string
  tPosition: (pos?: string) => string
}

const I18nContext = createContext<I18nContextType>({
  lang: "th",
  setLang: () => { },
  t: (key) => key,
  tLeaveType: (typeCode, dbName) => dbName || typeCode,
  tPosition: (pos) => pos || "",
})

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLang] = useState<Lang>("th")

  useEffect(() => {
    const saved = localStorage.getItem("eleave-lang") as Lang
    if (saved) setLang(saved)
  }, [])

  const changeLang = (newLang: Lang) => {
    setLang(newLang)
    localStorage.setItem("eleave-lang", newLang)
  }

  const t = (key: TranslationKey): string => {
    return translations[lang][key] || key
  }

  const tLeaveType = (typeCode: string, dbName?: string): string => {
    const key = `leaveType_${typeCode}` as TranslationKey
    const translated = translations[lang][key]
    if (translated) return translated
    return dbName || typeCode
  }

  const tPosition = (pos?: string): string => {
    if (!pos) return ""
    const key = `position_${pos}` as TranslationKey
    const translated = translations[lang][key]
    if (translated) return translated
    return pos
  }

  return (
    <I18nContext.Provider value={{ lang, setLang: changeLang, t, tLeaveType, tPosition }}>
      {children}
    </I18nContext.Provider>
  )
}

export const useI18n = () => useContext(I18nContext)

